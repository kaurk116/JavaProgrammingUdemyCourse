package JavaPogramming.Day3;

public class SwappingTwoNumber {
    public static void main(String[] args){
        int a=10;
        int b=20;
        System.out.println("Before swapping number" +a+ "--" +b);
         int t=a;
         a=b;
         b=t;
        System.out.println("After swapping number" +a+ "--" +b+ "--" +t);
    }
}
